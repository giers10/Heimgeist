function applyColorScheme(schemeName) {
  const scheme = colorSchemes[schemeName] || colorSchemes.Default
  if (!scheme) return

  for (const [key, value] of Object.entries(scheme)) {
    document.documentElement.style.setProperty(key, value)
  }
}

